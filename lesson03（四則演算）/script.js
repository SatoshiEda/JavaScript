let int02 = 30;
let ret;

console.log('---足し算-----------------------');
ret = int01 + int02;
console.log(ret);

console.log('---引き算-----------------------');
ret = int01 - int02;
console.log(ret);

console.log('---掛け算-----------------------');
ret = int01 * int02;
console.log(ret);

console.log('---割り算-----------------------');
ret = int01 / int02;
console.log(ret);


console.log('---剰余-----------------------');
ret = int01 % int02;
console.log(ret);


